//  基本八种类型

package Day01;

public class Demo2 {
    public static void main(String[] args) {
        // 定义字节型变量
        byte b = 20;
        System.out.println(b);
        // 定义短整型变量
        short s = 1000;
        System.out.println(s);
        //定义整型变量
        int i = 12345;
        System.out.println(i);
        //定义长整形变量
        long l = 51231231L;
        System.out.println(l);
        // 定义单精度浮点型变量
        float f = 5.11f;
        System.out.println(f);
        // 定义双精度浮点型变量
        double d = 2.111;
        System.out.println(d);
        //定义布尔型变量
        boolean bool =true;
        System.out.println(bool);
        //定义字符型变量
        char c = 'A';
        System.out.println(c);
        // 定义字符串变量
        String str = "你好 java";
        System.out.println(str);
    }
}
